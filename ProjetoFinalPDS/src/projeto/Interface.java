    private void botaoCadastrarAulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarAulaActionPerformed
        if (aulaComboTurmas.getSelectedIndex() != 0 && aulaComboDisc.getSelectedIndex() != 0) {
            Aula aula = new Aula();
            DefaultTableModel modelo = (DefaultTableModel) aulaTabelaAl.getModel();

            ArrayList<Integer> lista1 = new ArrayList<Integer>();
            ArrayList<String> lista2 = new ArrayList<String>();

            aula.setData(aulaCampoData.getText());
            aula.setAssunto(aulaCampoAssunto.getText());
            aula.setMatProf(matProfessor);
            aula.setCodTurma(parseInt(aulaComboTurmas.getItemAt(aulaComboTurmas.getSelectedIndex()).substring(0, 2)));
            aula.setCodisc(parseInt(aulaComboDisc.getItemAt(aulaComboDisc.getSelectedIndex()).substring(0, 2)));

            for (int i = 0; i < modelo.getRowCount(); i++) {
                lista1.add(parseInt(aulaTabelaAl.getValueAt(i, 0) + ""));
                lista2.add(aulaTabelaAl.getValueAt(i, 2) + "");
            }

            aula.setMatAlunos(lista1);
            aula.setStatus(lista2);
            funcoes.cadastrarAula(aula);
            JOptionPane.showMessageDialog(this, "Cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione apenas opções válidas!");
        }
    }//GEN-LAST:event_botaoCadastrarAulaActionPerformed

