    private void aulBotaoFaltasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aulBotaoFaltasActionPerformed
        if (tabelaAulas.getSelectedRow() != -1) {
            int codAula = parseInt(tabelaAulas.getValueAt(tabelaAulas.getSelectedRow(), 0) + "");

            DefaultTableModel modelo = (DefaultTableModel) tabelaFrequencia.getModel();
            modelo.setNumRows(0);

            ArrayList<Frequencia> frequencias = funcoes.retornaFrequencia(codAula);
            for (Frequencia freq : frequencias) {
                if (freq.getMatAluno() < 10) {
                    modelo.addRow(new Object[]{"0" + freq.getMatAluno() + " - " + freq.getNomeAluno(), freq.getPresenca()});
                } else {
                    modelo.addRow(new Object[]{freq.getMatAluno() + " - " + freq.getNomeAluno(), freq.getPresenca()});
                }

            }

            tabelaFrequencia.setModel(modelo);

            CardLayout card = (CardLayout) telas.getLayout();
            card.show(telas, "frequencias");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma Aula!");
        }
    }//GEN-LAST:event_aulBotaoFaltasActionPerformed

