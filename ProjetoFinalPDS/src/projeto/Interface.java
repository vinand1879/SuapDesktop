    public void attTabelaBoletim(int matAluno) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaBoletim.getModel();
        modelo.setNumRows(0);

        ArrayList<Notas> notas = funcoes.retornaNotasAluno(matAluno);
        for (Notas not : notas) {
            if (not.getCodisc() < 10) {
                modelo.addRow(new Object[]{"0" + not.getCodisc() + " - " + not.getNomeDisc(), not.getNota1(), not.getNota2(), not.getNota3(), not.getNota4()});
            } else {
                modelo.addRow(new Object[]{not.getCodisc() + " - " + not.getNomeDisc(), not.getNota1(), not.getNota2(), not.getNota3(), not.getNota4()});
            }

        }
    }

    public void attTabelaAulas(int codTurma, int codisc) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaAulas.getModel();
        modelo.setNumRows(0);

        ArrayList<Aula> aulas = funcoes.retornaAulas(codTurma, codisc);
        for (Aula aul : aulas) {
            modelo.addRow(new Object[]{aul.getCodAula(), aul.getData(), aul.getAssunto(), aul.getMatProf(), aul.getCodisc(), aul.getCodTurma()});
        }
    }

